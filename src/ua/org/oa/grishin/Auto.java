package ua.org.oa.grishin;

public class Auto {

	private String proizvoditel;
	private String marka;
	private int godVupuska;
	private int probeg;
	private int sostoyanie;
	
	//default constructor
	public Auto(){
		
	}
	
	//constructor with all parametres
	public Auto(String proizvoditel, String marka, int godVupuska, int probeg, int sostoyanie){
		this.proizvoditel = proizvoditel;
		this.marka = marka;
		this.godVupuska = godVupuska;
		this.probeg = probeg;
		this.sostoyanie = sostoyanie; 
	}
	
	/**
	 * @return the proizvoditel
	 */
	public String getProizvoditel() {
		return proizvoditel;
	}

	/**
	 * @param proizvoditel the proizvoditel to set
	 */
	public void setProizvoditel(String proizvoditel) {
		this.proizvoditel = proizvoditel;
	}

	/**
	 * @return the marka
	 */
	public String getMarka() {
		return marka;
	}

	/**
	 * @param marka the marka to set
	 */
	public void setMarka(String marka) {
		this.marka = marka;
	}

	/**
	 * @return the godVupuska
	 */
	public int getGodVupuska() {
		return godVupuska;
	}

	/**
	 * @param godVupuska the godVupuska to set
	 */
	public void setGodVupuska(int godVupuska) {
		this.godVupuska = godVupuska;
	}

	/**
	 * @return the probeg
	 */
	public int getProbeg() {
		return probeg;
	}

	/**
	 * @param probeg the probeg to set
	 */
	public void setProbeg(int probeg) {
		this.probeg = probeg;
	}

	/**
	 * @return the sostoyanie
	 */
	public int getSostoyanie() {
		return sostoyanie;
	}

	/**
	 * @param sostoyanie the sostoyanie to set
	 */
	public void setSostoyanie(int sostoyanie) {
		this.sostoyanie = sostoyanie;
	}

	/* (non-Javadoc)
	 * @see java.lang.Object#toString()
	 */
	@Override
	public String toString() {
		return "Auto [proizvoditel=" + proizvoditel + ", marka=" + marka
				+ ", godVupuska=" + godVupuska + ", probeg=" + probeg
				+ ", sostoyanie=" + sostoyanie + "]";
	}
}
